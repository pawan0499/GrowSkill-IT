package JavaAssignment;

public class Assignment7 {

	public static void main(String[] args) {

//		1.Print Sum of Natural Numbers
//		Write a Java program to find the sum of first 50 natural numbers using a loop.

		int sum = 0;

		for (int num1 = 1; num1 <= 50; num1++) {
			sum = sum + num1;

		}
		System.out.println("sum 50 Natural numbers :- " + sum);

	}

}
